import type { Metadata } from "next";
import localFont from "next/font/local";
import "./globals.css";

// components
import Sidebar from "../components/Sidenav";

// providers
import ToasterProvider from "../providers/ToasterProvider";
import ModalProvider from "../providers/ModalProvider";
import { IHandleActionAnnouncement } from "../types/announcement";
import { createAnnouncement } from "../actions/announcements";
import { getAllByTeacher, getAllClasses } from "../actions/classes";

const geistSans = localFont({
    src: "./fonts/GeistVF.woff",
    variable: "--font-geist-sans",
    weight: "100 900",
});

const geistMono = localFont({
    src: "./fonts/GeistMonoVF.woff",
    variable: "--font-geist-mono",
    weight: "100 900",
});

export const metadata: Metadata = {
    title: "Create Next App",
    description: "Generated by create next app",
};

export default function RootLayout({
    children,
}: Readonly<{
    children: React.ReactNode;
}>) {

    const handleActionsAnnouncment: IHandleActionAnnouncement = {
        handleActionCreate: async (data) => {
            "use server";
            return createAnnouncement(data);
        },
        handleActionGetClassesByTeacher: async (authorId) => {
            "use server";
            return getAllByTeacher(authorId);
        },
        handleActionGetAllClasses: async () => {
            "use server";
            return getAllClasses();
        }
    }

    return (
        <html lang="en">
            <body
                className={`${geistSans.variable} ${geistMono.variable} antialiased flex`}
            >
                <ModalProvider handleActionsAnnouncement={handleActionsAnnouncment} />
                <ToasterProvider />
                <Sidebar />
                {children}
            </body>
        </html>
    );
}
